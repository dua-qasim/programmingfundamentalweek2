#include <iostream>
using namespace std;

bool isGoalScored(char field[7][16])
{
    for(int i=0; i<7; i++)
    {
        for(int j=0; j<16; j++)
        {
            if(field[i][j] == 'O')
            {
                if(j == 0 || j == 15)
                {
                    return true;
                }
            }
        }
    }

    return false;
}

int main()
{
    char field[7][16] =
    {
        {' ','#',' ',' ',' ',' ',' ',' ',' ',' ',' ',' ',' ','#',' ',' '},
        {' ','#',' ',' ',' ',' ',' ',' ',' ',' ',' ',' ',' ','#',' ',' '},
        {' ','#','#','#','#','#','#','#','#','#','#','#','#','#',' ',' '},
        {' ',' ','#',' ','O',' ',' ',' ',' ',' ',' ',' ','#',' ',' ',' '},
        {' ',' ','#',' ',' ',' ',' ',' ',' ',' ',' ',' ','#',' ',' ',' '},
        {' ',' ',' ','#',' ',' ',' ',' ',' ',' ',' ','#',' ',' ',' ',' '},
        {' ',' ',' ',' ','#',' ',' ',' ',' ',' ','#',' ',' ',' ',' ',' '}
    };

    if(isGoalScored(field))
    {
        cout << "True";
    }
    else
    {
        cout << "False";
    }

    return 0;
}